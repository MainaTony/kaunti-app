function GameScreen({
  questionIdx, total, score, streak,
  target, options, selected, setSelected, submitAnswer,
  status, feedbackMsg, confettiKey, pointsPopup,
  soundEnabled, setSoundEnabled,
}) {
  const progress = ((questionIdx + (status !== 'asking' ? 1 : 0)) / total) * 100;

  return (
    <div className="relative min-h-screen flex flex-col px-4 pt-3 pb-4 sm:px-6 sm:pt-4 max-w-3xl mx-auto">
      {/* confetti layer */}
      {confettiKey > 0 && status === 'correct' && <Confetti key={confettiKey} />}

      {/* TOP BAR */}
      <div className="flex items-center justify-between gap-2 mb-3">
        <div className="flex items-center gap-1.5 px-2.5 py-1.5 rounded-full bg-white border border-slate-200 shadow-sm">
          <span className="text-[10px] font-mono uppercase tracking-widest text-slate-400">Q</span>
          <span className="font-display font-bold text-sm text-slate-800">
            {questionIdx + 1}<span className="text-slate-400 font-medium">/{total}</span>
          </span>
        </div>

        <div className="flex items-center gap-1.5">
          <div key={`score-${score}`} className="flex items-center gap-1.5 px-3 py-1.5 rounded-full bg-indigo-50 border border-indigo-200 kt-pop">
            <span className="font-display font-extrabold text-indigo-600 text-sm">{score.toLocaleString()}</span>
            <span className="text-[9px] font-mono uppercase tracking-widest text-indigo-400">pts</span>
          </div>
          <div key={`streak-${streak}`}
               className={`flex items-center gap-1 px-2.5 py-1.5 rounded-full border transition ${
                 streak >= 3 ? 'bg-rose-50 border-rose-200' : 'bg-white border-slate-200 shadow-sm'
               } ${streak > 0 ? 'kt-bump' : ''}`}>
            <Flame className={`w-3.5 h-3.5 ${streak >= 3 ? 'text-rose-400' : 'text-slate-400'}`} />
            <span className="font-display font-bold text-sm text-slate-800">{streak}</span>
          </div>
          <button onClick={() => setSoundEnabled(!soundEnabled)}
            className="p-1.5 rounded-full bg-white border border-slate-200 shadow-sm hover:bg-slate-50 transition text-slate-600">
            {soundEnabled ? <Volume2 className="w-4 h-4" /> : <VolumeX className="w-4 h-4 opacity-40" />}
          </button>
        </div>
      </div>

      {/* PROGRESS */}
      <div className="h-1.5 rounded-full bg-slate-100 overflow-hidden mb-4">
        <div className="h-full rounded-full transition-all duration-500"
          style={{ width: `${progress}%`, background: 'linear-gradient(90deg, #818CF8, #6366F1, #4F46E5)' }} />
      </div>

      {/* QUESTION */}
      <div className="text-center mb-1">
        <div className="text-[10px] font-mono uppercase tracking-[0.3em] text-slate-400">
          Which county is highlighted?
        </div>
      </div>

      {/* MAP */}
      <div className="relative flex-1 min-h-0 flex items-center justify-center my-2">
        <div className="relative w-full max-w-md mx-auto" style={{ aspectRatio: '7/9' }}>
          <KenyaMap
            targetCounty={target}
            status={status}
          />
          {/* Floating feedback text */}
          {feedbackMsg && (
            <div className="absolute left-1/2 top-1/2 -translate-x-1/2 -translate-y-1/2 pointer-events-none z-30 text-center">
              <div className={`font-display font-extrabold kt-float ${
                status === 'correct' ? 'text-emerald-600' : 'text-rose-600'
              }`}
                style={{
                  fontSize: 'clamp(2rem, 7vw, 3.25rem)',
                  textShadow: status === 'correct'
                    ? '0 0 36px rgba(16,185,129,0.55), 0 4px 12px rgba(0,0,0,0.1)'
                    : '0 0 36px rgba(239,68,68,0.55), 0 4px 12px rgba(0,0,0,0.1)',
                }}>
                {feedbackMsg}
              </div>
              {pointsPopup && status === 'correct' && (
                <div className="font-display font-bold text-indigo-600 text-xl mt-1 kt-float" style={{ animationDelay: '0.1s' }}>
                  {pointsPopup.value}
                </div>
              )}
            </div>
          )}
        </div>
      </div>

      {/* OPTIONS */}
      <div className={`grid grid-cols-2 gap-2.5 ${status === 'wrong' ? 'kt-shake' : ''}`}>
        {options.map((opt, i) => {
          const isSel = selected === opt;
          const isAnswered = status !== 'asking';
          const isCorrectOpt = target && opt === target.name;

          let cls = 'bg-white border-slate-200 hover:border-slate-400 hover:shadow-md text-slate-800 shadow-sm';
          if (isAnswered) {
            if (isCorrectOpt) cls = 'bg-emerald-50 border-emerald-400 text-emerald-800 shadow-sm';
            else if (isSel)   cls = 'bg-rose-50 border-rose-400 text-rose-800 shadow-sm';
            else              cls = 'bg-slate-50 border-slate-100 text-slate-300';
          } else if (isSel)   cls = 'bg-indigo-50 border-indigo-400 text-indigo-900 shadow-md';

          return (
            <button key={opt} disabled={isAnswered}
              onClick={() => setSelected(opt)}
              className={`relative px-3 py-3.5 rounded-2xl border-2 font-display font-semibold text-sm sm:text-base transition-all ${cls} ${
                !isAnswered ? 'active:scale-95 hover:scale-[1.02]' : ''
              } ${isCorrectOpt && isAnswered ? 'kt-pop' : ''}`}>
              <span className="text-[10px] font-mono text-slate-400 mr-1.5">{String.fromCharCode(65 + i)}</span>
              {opt}
            </button>
          );
        })}
      </div>

      {/* SUBMIT */}
      <button onClick={submitAnswer}
        disabled={!selected || status !== 'asking'}
        className="mt-3 w-full py-4 rounded-2xl font-display font-extrabold text-lg tracking-wide transition disabled:cursor-not-allowed enabled:hover:scale-[1.02] active:scale-[0.98]"
        style={{
          background: selected && status === 'asking'
            ? 'linear-gradient(135deg, #818CF8, #6366F1)'
            : '#F1F5F9',
          color: selected && status === 'asking' ? '#FFFFFF' : '#94A3B8',
          boxShadow: selected && status === 'asking'
            ? '0 14px 36px -10px rgba(99, 102, 241, 0.45)'
            : 'none',
        }}>
        {status === 'asking' ? 'Submit Answer' : 'Loading next…'}
      </button>
    </div>
  );
}

