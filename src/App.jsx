// AUDIO — synthesized via Web Audio API (no external files)
// ════════════════════════════════════════════════════════════════════════════
let audioCtx = null;
const getCtx = () => {
  if (!audioCtx && typeof window !== 'undefined') {
    const AC = window.AudioContext || window.webkitAudioContext;
    if (AC) audioCtx = new AC();
  }
  return audioCtx;
};

const playSound = (kind) => {
  const ctx = getCtx();
  if (!ctx) return;
  try {
    if (ctx.state === 'suspended') ctx.resume();
    const now = ctx.currentTime;

    const tone = (freq, start, dur, vol = 0.15, type = 'triangle') => {
      const o = ctx.createOscillator();
      const g = ctx.createGain();
      o.type = type;
      o.frequency.value = freq;
      g.gain.setValueAtTime(0.0001, now + start);
      g.gain.exponentialRampToValueAtTime(vol, now + start + 0.02);
      g.gain.exponentialRampToValueAtTime(0.0001, now + start + dur);
      o.connect(g).connect(ctx.destination);
      o.start(now + start);
      o.stop(now + start + dur + 0.05);
    };

    if (kind === 'correct') {
      tone(523.25, 0.00, 0.35);
      tone(659.25, 0.07, 0.35);
      tone(783.99, 0.14, 0.40);
      tone(1046.5, 0.21, 0.45, 0.18);
    } else if (kind === 'wrong') {
      const o = ctx.createOscillator();
      const g = ctx.createGain();
      o.type = 'sawtooth';
      o.frequency.setValueAtTime(220, now);
      o.frequency.exponentialRampToValueAtTime(70, now + 0.4);
      g.gain.setValueAtTime(0.12, now);
      g.gain.exponentialRampToValueAtTime(0.0001, now + 0.4);
      o.connect(g).connect(ctx.destination);
      o.start();
      o.stop(now + 0.45);
    } else if (kind === 'tick') {
      tone(720, 0, 0.05, 0.06, 'sine');
    } else if (kind === 'whoosh') {
      const o = ctx.createOscillator();
      const g = ctx.createGain();
      o.type = 'sine';
      o.frequency.setValueAtTime(140, now);
      o.frequency.exponentialRampToValueAtTime(480, now + 0.22);
      g.gain.setValueAtTime(0.0001, now);
      g.gain.exponentialRampToValueAtTime(0.08, now + 0.08);
      g.gain.exponentialRampToValueAtTime(0.0001, now + 0.22);
      o.connect(g).connect(ctx.destination);
      o.start();
      o.stop(now + 0.25);
    } else if (kind === 'win') {
      [523.25, 659.25, 783.99, 1046.5, 1318.5].forEach((f, i) => tone(f, i * 0.11, 0.5, 0.16));
    }
  } catch (_) {}
};

