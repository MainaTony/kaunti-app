    const t = pool[Math.floor(Math.random() * pool.length)];
    recentRef.current = [t.name, ...recentRef.current].slice(0, 10);

    // distractors: prefer geographic neighbours (real adjacency), then random elsewhere
    const neighbourSet = new Set(t.neighbors || []);
    const neighbourPool = COUNTIES.filter(c => neighbourSet.has(c.name));
    const farPool       = COUNTIES.filter(c => c.name !== t.name && !neighbourSet.has(c.name));

    const distractors = [];
    while (distractors.length < 2 && neighbourPool.length) {
      distractors.push(neighbourPool.splice(Math.floor(Math.random() * neighbourPool.length), 1)[0]);
    }
    while (distractors.length < 3 && farPool.length) {
      distractors.push(farPool.splice(Math.floor(Math.random() * farPool.length), 1)[0]);
    }

    const opts = [t, ...distractors]
      .map(c => ({ c, k: Math.random() }))
      .sort((a, b) => a.k - b.k)
