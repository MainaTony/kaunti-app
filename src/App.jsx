// CONFETTI — canvas burst
// ════════════════════════════════════════════════════════════════════════════
function Confetti() {
  const ref = useRef(null);
  useEffect(() => {
    const canvas = ref.current;
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    const rect = canvas.getBoundingClientRect();
    const dpr = window.devicePixelRatio || 1;
    canvas.width = rect.width * dpr;
    canvas.height = rect.height * dpr;
    ctx.scale(dpr, dpr);

    const colors = ['#6366F1', '#22d3ee', '#a78bfa', '#f472b6', '#34d399', '#fb7185', '#818CF8'];
    const cx = rect.width / 2;
    const cy = rect.height * 0.4;
    const particles = [];
    for (let i = 0; i < 120; i++) {
      const angle = -Math.PI / 2 + (Math.random() - 0.5) * Math.PI * 0.9;
      const speed = 8 + Math.random() * 10;
      particles.push({
        x: cx, y: cy,
        vx: Math.cos(angle) * speed,
        vy: Math.sin(angle) * speed,
        g: 0.32 + Math.random() * 0.18,
        color: colors[(Math.random() * colors.length) | 0],
        size: 4 + Math.random() * 7,
        rot: Math.random() * Math.PI * 2,
        vr: (Math.random() - 0.5) * 0.4,
        life: 70 + Math.random() * 40,
        age: 0,
      });
    }

    let raf;
    const tick = () => {
      ctx.clearRect(0, 0, rect.width, rect.height);
      let alive = false;
      for (const p of particles) {
        if (p.age >= p.life) continue;
        alive = true;
        p.x += p.vx;
        p.y += p.vy;
        p.vy += p.g;
        p.vx *= 0.99;
        p.rot += p.vr;
        p.age++;
        const alpha = Math.max(0, 1 - p.age / p.life);
        ctx.save();
        ctx.globalAlpha = alpha;
        ctx.translate(p.x, p.y);
        ctx.rotate(p.rot);
        ctx.fillStyle = p.color;
        ctx.fillRect(-p.size / 2, -p.size / 3, p.size, p.size / 1.6);
        ctx.restore();
      }
      if (alive) raf = requestAnimationFrame(tick);
    };
    raf = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(raf);
  }, []);

  return <canvas ref={ref} className="absolute inset-0 w-full h-full pointer-events-none z-20" />;
}

